namespace MightAndMagic1Trainer.Game;

/// <summary>
/// One monster from the game's bestiary. <see cref="HpBase"/> is the stored base hit
/// points (the game adds a small random bonus when a group is generated); <see cref="Damage"/>
/// is the maximum damage of one melee attack and <see cref="Attacks"/> how many such attacks
/// the monster makes per round. <see cref="Experience"/> is split among surviving party members.
/// </summary>
public sealed record GameMonster(
    int Id, string Name, int MaxCount, int HpBase, int ArmorClass,
    int Damage, int Attacks, int Speed, int Experience)
{
    /// <summary>Difficulty bucket, derived from the table position (see <see cref="MonsterBook"/>).</summary>
    public string Group => MonsterBook.GroupOf(Id);

    /// <summary>Detail line for the reference list.</summary>
    public string DetailText =>
        $"#{Id} · HP {HpBase}+ · AC {ArmorClass} · damage {Damage} ×{Attacks} · speed {Speed} · up to {MaxCount} appear";

    /// <summary>Right-aligned accent tag: the experience for a kill.</summary>
    public string ExpTag => $"{Experience:N0} XP";
}

